// Columns PINS
const GPIO_TypeDef* columns_bank[COLUMNS_NUMBER] = {GPIOC, GPIOC, GPIOC, GPIOC};
const uint16_t columns_pin[COLUMNS_NUMBER] = {GPIO_PIN_9, GPIO_PIN_10, GPIO_PIN_11, GPIO_PIN_8};

// Rows PINS
const GPIO_TypeDef* rows_bank[ROWS_NUMBER] = {GPIOC, GPIOC, GPIOC, GPIOC};
const uint16_t rows_pin[ROWS_NUMBER]      = {GPIO_PIN_12, GPIO_PIN_13, GPIO_PIN_2, GPIO_PIN_3};

GPIO_PinState keyboard[COLUMNS_NUMBER * ROWS_NUMBER] = {0};
GPIO_PinState keyboard_old[COLUMNS_NUMBER * ROWS_NUMBER] = {0};
int keyboard_debounce[COLUMNS_NUMBER * ROWS_NUMBER];
static const char keymap[] = "0123456789ABCDEF";

/**
 * @brief Read keyboard key pressed and send the equivalent value to the UART terminal
 */
void KeyboardReading (void)
{
	static uint8_t col_index = 0;

	// For every rows
	for(int row_index = 0; row_index < ROWS_NUMBER; row_index++)
	{
		// If debounce period is over
		if(keyboard_debounce[row_index*COLUMNS_NUMBER+col_index] == 0)
		{
			keyboard[row_index*COLUMNS_NUMBER+col_index] = HAL_GPIO_ReadPin((GPIO_TypeDef*)rows_bank[row_index], rows_pin[row_index]);
			// Comparation between the current key pressed and the old one
			if(keyboard[row_index*COLUMNS_NUMBER+col_index] != keyboard_old[row_index*COLUMNS_NUMBER+col_index])
			{
				// Check if the old key is not pressed
				if(keyboard_old[row_index*COLUMNS_NUMBER+col_index] == GPIO_PIN_SET)
				{
					HAL_UART_Transmit(&huart2,(uint8_t*) &keymap[row_index*COLUMNS_NUMBER+col_index], 1, 10); // map[row_index*COLUMNS_NUMBER+col_index]
					keyboard_debounce[row_index*COLUMNS_NUMBER+col_index] = KBOARD_DEBOUNCE_CYCLES; // reload debounce counter
				}
				// Set the old keybord key to the pressed one
				keyboard_old[row_index*COLUMNS_NUMBER+col_index] = keyboard[row_index*COLUMNS_NUMBER+col_index];
			}
		}

		// Reset debounce
		else
		{
			keyboard_debounce[row_index*COLUMNS_NUMBER+col_index] = GPIO_PIN_RESET;
		}
	}

	// This is done to maintain consistency with the "set" and "reset" defines
	for(int i = 0; i < COLUMNS_NUMBER; i++)
	{
		HAL_GPIO_WritePin((GPIO_TypeDef*)columns_bank[i], columns_pin[i], (col_index == i) ? GPIO_PIN_SET : GPIO_PIN_RESET);
	}

	// Work as a loop for the columns because the for loop is not the best in this case
	if(col_index >= COLUMNS_NUMBER-1)
	{
		col_index = 0;
	}
	else
	{
		col_index++;
	}
}



